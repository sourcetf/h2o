#!/bin/bash

# 极限负载测试脚本：测试高负载下的稳定性和错误率
# 使用 ab (Apache Bench) 和 wrk，测试大量并发连接、长连接以及突发流量

set -e

PROXY_URL="http://127.0.0.1:8080/"

echo "=========================================="
echo "          极限负载测试脚本                "
echo "=========================================="

if ! command -v wrk &> /dev/null; then
    echo "错误: 未找到 wrk，请先安装"
    exit 1
fi

if ! command -v ab &> /dev/null; then
    echo "错误: 未找到 ab (Apache Bench)，请先安装 (apt install apache2-utils)"
    exit 1
fi

echo "------------------------------------------"
echo "1. 突发大流量测试 (短时间、超高并发)"
echo "------------------------------------------"
echo "并发: 5000, 持续时间: 15s"
wrk -t 12 -c 5000 -d 15s --timeout 5s $PROXY_URL
echo ""

echo "------------------------------------------"
echo "2. 长连接保活测试 (Keep-Alive)"
echo "------------------------------------------"
echo "总请求数: 200000, 并发: 1000"
ab -k -n 200000 -c 1000 $PROXY_URL
echo ""

echo "------------------------------------------"
echo "3. 并发建立连接测试 (短连接，测试 Accept 性能)"
echo "------------------------------------------"
echo "不使用 Keep-Alive，模拟新建连接请求"
ab -n 100000 -c 1000 $PROXY_URL
echo ""

echo "=========================================="
echo "          极限负载测试完成                "
echo "          请检查错误率 (Failed requests)"
echo "=========================================="
